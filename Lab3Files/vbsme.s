Move_Down:
    addi $t5, $t1, 1     # row = top + 1
    move $t4, $t2        # col = right
Move_Down_Loop:
    slt  $t6, $t5, $t3   # if row < bottom
    beq  $t6, $zero, Move_Left

    # Compute SAD at ($t5, $t4)
    jal  Compute_SAD

    addi $t5, $t5, 1     # row++
    j    Move_Down_Loop

