# Begin subroutine
vbsme:  
    li      $v0, 0              # reset $v0 and $V1
    li      $v1, 0

# insert your code here

##################

    # Load dimensions from asize array
    lw   $s0, 0($a0)    # Frame rows (i)
    lw   $s1, 4($a0)    # Frame cols (j)
    lw   $s2, 8($a0)    # Window rows (k)
    lw   $s3, 12($a0)   # Window cols (l)

